    private void BtnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAceptarActionPerformed
        try {
            ArrayList<Problema> listaDeProblemas = new ArrayList();
            for(Object unPro : modeloListaProblema.toArray()){
                Problema temp = (Problema) unPro;
                listaDeProblemas.add(temp);
            }
            ArrayList<RepuestoLinea> listaDeRepuestos = new ArrayList();
            for(Object unRep : modeloListaRepuesto.toArray()){
                RepuestoLinea temp = (RepuestoLinea) unRep;
                listaDeRepuestos.add(temp);
            }
            this.emp.agregarServicio(jTextArea1.getText(), 0, this.unaReserva,listaDeProblemas, listaDeRepuestos);
            JOptionPane.showMessageDialog(null, "Se agrego el servicio correctamente.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_BtnAceptarActionPerformed

