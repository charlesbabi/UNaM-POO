    private void BtnAgregarProblemaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarProblemaActionPerformed
        // Boton Agregar problemas a la lista
        if(ComboProblemas.getSelectedIndex() > -1){
            Problema temp = (Problema) ComboProblemas.getSelectedItem();
            if(!modeloListaProblema.contains(temp)){
                modeloListaProblema.addElement(temp);
            }
        }
    }//GEN-LAST:event_BtnAgregarProblemaActionPerformed

    private void BtnQuitarProblemaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnQuitarProblemaActionPerformed
        // Boton Quitar problemas a la lista
        if(ListaProblemas.getSelectedIndex() > -1){
            Problema temp = (Problema) ListaProblemas.getSelectedValue();
            modeloListaProblema.removeElement(temp);
        }
    }//GEN-LAST:event_BtnQuitarProblemaActionPerformed

    private void BtnAgregarRepuestoLineaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarRepuestoLineaActionPerformed
        // Boton Agregar Repuestos a la lista
        try {
            if(ComboRepuestos.getSelectedIndex() > -1){
                Repuesto temp = (Repuesto) ComboRepuestos.getSelectedItem();
                RepuestoLinea lineaTemp = new RepuestoLinea(Integer.parseInt(TxtCantidad.getText()), temp);
                 if(!modeloListaRepuesto.contains(lineaTemp)){
                     modeloListaRepuesto.addElement(lineaTemp);
                 }
            }
        } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_BtnAgregarRepuestoLineaActionPerformed

    private void BtnQuitarRepuestoLineaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnQuitarRepuestoLineaActionPerformed
        // Boton Quitar Repuesto a la lista
        if(ListaRepuesto.getSelectedIndex() > -1){
            Repuesto temp = (Repuesto) ListaRepuesto.getSelectedValue();
            modeloListaRepuesto.removeElement(temp);
        }
    }//GEN-LAST:event_BtnQuitarRepuestoLineaActionPerformed

    private void cargarComboProblemas(){
        modeloComboProblemas.removeAllElements();
        Iterator<Problema> it = this.emp.getProblemas().iterator();
        while(it.hasNext()){
            modeloComboProblemas.addElement(it.next());
        }
    }
    
    private void cargarComboRespuestos(){
        modeloComboRespuesto.removeAllElements();
        Iterator<Repuesto> it = this.emp.getRepuestos().iterator();
        while(it.hasNext()){
            modeloComboProblemas.addElement(it.next());
        }
    }
    
    
