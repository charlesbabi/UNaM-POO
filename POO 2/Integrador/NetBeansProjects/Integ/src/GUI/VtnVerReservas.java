    private void llenarReservas(GregorianCalendar unDia) throws Exception{
        if(espe != null){
            try{
                Iterator<Reserva> itReservas = this.emp.verReservas(espe, unDia).iterator();
                modeloListaReservas.removeAllElements();
                while(itReservas.hasNext()){
                    modeloListaReservas.addElement(itReservas.next());
                }    
            }catch (Exception ex) {
                modeloListaReservas.removeAllElements();
            }   
        }
    }
    
